-- 创建靓号表
CREATE TABLE IF NOT EXISTS lucky_numbers (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    number VARCHAR(50) NOT NULL UNIQUE COMMENT '靓号数字',
    price DECIMAL(10,2) NOT NULL COMMENT '价格（聊币）',
    tier ENUM('LIMITED', 'SUPER', 'TOP_TIER') NOT NULL COMMENT '等级：限量、超级、顶级',
    status ENUM('AVAILABLE', 'SOLD', 'RESERVED') NOT NULL DEFAULT 'AVAILABLE' COMMENT '状态：可购买、已售出、已预订',
    owner_id BIGINT COMMENT '拥有者ID',
    purchase_time DATETIME COMMENT '购买时间',
    validity_days INT COMMENT '有效期（天）',
    expire_time DATETIME COMMENT '过期时间',
    description TEXT COMMENT '描述',
    is_special BOOLEAN NOT NULL DEFAULT FALSE COMMENT '是否特殊靓号',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    
    INDEX idx_number (number),
    INDEX idx_status (status),
    INDEX idx_tier (tier),
    INDEX idx_owner_id (owner_id),
    INDEX idx_price (price),
    INDEX idx_is_special (is_special),
    INDEX idx_expire_time (expire_time)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='靓号表';

